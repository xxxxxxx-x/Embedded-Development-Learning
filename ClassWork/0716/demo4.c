#include <stdio.h>

int main(int argc, const char *argv[]) {
    // 定义一个5行5列的整型二维数组
    signed int array[5][5];

    // 使用循环对数组进行赋值
    for (int i = 0; i < 5; i++) {
        for (int j = 0; j < 5; j++) {
            array[i][j] = i * 5 + j + 1; // 按照行列顺序逐个赋值
        }
    }

    // 输出二维数组的内容
    printf("二维数组内容如下:\n");
    for (int i = 0; i < 5; i++) {
        for (int j = 0; j < 5; j++) {
            printf("%-3d ", array[i][j]); // 输出每个元素
        }
        printf("\n");
    }

    return 0;
}
