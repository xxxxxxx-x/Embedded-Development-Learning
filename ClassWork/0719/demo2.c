#include <stdbool.h>
#include <stdio.h>

/****************************************************************************************************************************************************************
 * @name: IsUppercase
 * @brief: 判断字符是否为大写字母
 * @param {char} ch 输入的字符
 * @return {bool} 如果是大写字母返回 true，否则返回 false
 * @date: 2024-07-19 14:38:47
 * @version: 1.0
 * @note: 大写字母的 ASCII 值范围是 65 ('A') 到 90 ('Z')
 *****************************************************************************************************************************************************************/
bool IsUppercase(char ch) {
  // 大写字母的 ASCII 值范围是 65 ('A') 到 90 ('Z')
  return ch >= 'A' && ch <= 'Z';
}

