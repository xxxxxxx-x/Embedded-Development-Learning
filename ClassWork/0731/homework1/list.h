#ifndef LIST_H
#define LIST_H

#include <stdbool.h>
#include <stdio.h>
#include <stdlib.h>

// 定义单链表的节点结构体
typedef struct ListNode {
  int data;              // 节点数据
  struct ListNode *next; // 指向下一个节点的指针
} ListNode;

/****************************************************************************************************************************************************************
 * @name: create_node
 * @brief: 创建一个新的节点
 * @param {int} value 节点数据
 * @return {ListNode*} 新创建的节点指针
 * @date: 2024-08-01 13:10:22
 * @version: 1.0
 * @note: 
*****************************************************************************************************************************************************************/
ListNode *create_node(int value);

/****************************************************************************************************************************************************************
 * @name: insert_head
 * @brief: 在链表头部插入一个新节点
 * @param {ListNode**} head 链表头指针的指针
 * @param {int} value 新节点的数据
 * @return {bool} 插入成功返回 true，否则返回 false
 * @date: 2024-08-01 13:10:28
 * @version: 1.0
 * @note: 
*****************************************************************************************************************************************************************/
bool insert_head(ListNode **head, int value);

/****************************************************************************************************************************************************************
 * @name: insert_tail
 * @brief: 在链表尾部插入一个新节点
 * @param {ListNode**} head 链表头指针的指针
 * @param {int} value 新节点的数据
 * @return {bool} 插入成功返回 true，否则返回 false
 * @date: 2024-08-01 13:10:35
 * @version: 1.0
 * @note: 
*****************************************************************************************************************************************************************/
bool insert_tail(ListNode **head, int value);

/****************************************************************************************************************************************************************
 * @name: insert_at
 * @brief: 在链表的指定位置插入一个新节点
 * @param {ListNode**} head 链表头指针的指针
 * @param {int} value 新节点的数据
 * @param {int} position 插入位置
 * @return {void}
 * @date: 2024-08-01 13:10:41
 * @version: 1.0
 * @note: 
*****************************************************************************************************************************************************************/
void insert_at(ListNode **head, int value, int position);

/****************************************************************************************************************************************************************
 * @name: delete_head
 * @brief: 删除链表的头节点
 * @param {ListNode**} head 链表头指针的指针
 * @return {void}
 * @date: 2024-08-01 13:10:47
 * @version: 1.0
 * @note: 
*****************************************************************************************************************************************************************/
void delete_head(ListNode **head);

/****************************************************************************************************************************************************************
 * @name: delete_tail
 * @brief: 删除链表的尾节点
 * @param {ListNode**} head 链表头指针的指针
 * @return {void}
 * @date: 2024-08-01 13:10:53
 * @version: 1.0
 * @note: 
*****************************************************************************************************************************************************************/
void delete_tail(ListNode **head);

/****************************************************************************************************************************************************************
 * @name: delete_at
 * @brief: 删除链表指定位置的节点
 * @param {ListNode**} head 链表头指针的指针
 * @param {int} position 删除的位置
 * @return {void}
 * @date: 2024-08-01 13:10:57
 * @version: 1.0
 * @note: 
*****************************************************************************************************************************************************************/
void delete_at(ListNode **head, int position);

/****************************************************************************************************************************************************************
 * @name: update_value
 * @brief: 更新链表指定位置的节点数据
 * @param {ListNode*} head 链表头指针
 * @param {int} position 更新的位置
 * @param {int} new_value 新数据
 * @return {void}
 * @date: 2024-08-01 13:11:04
 * @version: 1.0
 * @note: 
*****************************************************************************************************************************************************************/
void update_value(ListNode *head, int position, int new_value);

/****************************************************************************************************************************************************************
 * @name: search_value
 * @brief: 搜索链表中第一个匹配指定数据的节点
 * @param {ListNode*} head 链表头指针
 * @param {int} value 搜索的数据
 * @return {ListNode*} 找到的节点指针，未找到返回 NULL
 * @date: 2024-08-01 13:11:10
 * @version: 1.0
 * @note: 
*****************************************************************************************************************************************************************/
ListNode *search_value(ListNode *head, int value);

/****************************************************************************************************************************************************************
 * @name: display_list
 * @brief: 打印链表中的所有节点数据
 * @param {ListNode*} head 链表头指针
 * @return {void}
 * @date: 2024-08-01 13:11:15
 * @version: 1.0
 * @note: 
*****************************************************************************************************************************************************************/
void display_list(ListNode *head);

#endif // LIST_H
