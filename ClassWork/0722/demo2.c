#include <stdio.h>

/****************************************************************************************************************************************************************
 * @name         : sumArray1
 * @brief        : 计算3x3二维数组的元素之和
 * @param {int(*)[3]} arr - 指向3x3数组的指针
 * @return       : 数组元素之和
 * @date         : 2024-07-22 15:26:12
 * @version      : 1.0
 * @note         : 使用指针参数的版本
 *****************************************************************************************************************************************************************/
int sumArray1(int (*arr)[3]) {
  int sum = 0;
  for (int i = 0; i < 3; i++) {
    for (int j = 0; j < 3; j++) {
      sum += arr[i][j];
    }
  }
  return sum;
}

/****************************************************************************************************************************************************************
 * @name         : sumArray2
 * @brief        : 计算3x3二维数组的元素之和
 * @param {int[3][3]} arr - 3x3数组
 * @return       : 数组元素之和
 * @date         : 2024-07-22 15:26:27
 * @version      : 1.0
 * @note         : 使用数组参数的版本
 *****************************************************************************************************************************************************************/
int sumArray2(int arr[3][3]) {
  int sum = 0;
  // 使用常量代替 sizeof 运算来计算数组的维度
  for (int i = 0; i < 3; i++) {
    for (int j = 0; j < 3; j++) {
      sum += arr[i][j];
    }
  }
  return sum;
}

int main(int argc, const char *argv[]) {
  int arr[3][3] = {1, 2, 3, 4, 5, 6, 7, 8, 9};
  // 打印函数sumArray1的结果
  printf("使用 sumArray1函数 求和: %d\n", sumArray1(arr));
  // 打印函数sumArray2的结果
  printf("使用 sumArray2函数 求和: %d\n", sumArray2(arr));
  return 0;
}
