#include <stdio.h>

/****************************************************************************************************************************************************************
 * @name: max_same_char
 * @brief: 计算字符串 `s` 中连续相同字符的最大长度。
 * @param {char} *s - 要分析的输入字符串。
 * @return {int} - 连续相同字符的最大长度。
 * @date: 2024-07-09 13:11:47
 * @version: 1.0
 * @note: 
*****************************************************************************************************************************************************************/
int max_same_char(char *s)
{
    int max_length = 0;       // 最大连续相同字符的长度
    int current_length = 1;   // 当前连续相同字符的长度

    while (*s != '\0')
    {
        if (*(s + 1) != '\0' && *s == *(s + 1))
        {
            current_length++;  // 如果当前字符与下一个字符相同，增加当前长度
        }
        else
        {
            if (current_length > max_length)
            {
                max_length = current_length;  // 更新最大长度
            }
            current_length = 1;  // 重置当前长度
        }
        s++;  // 移动到下一个字符
    }

    return max_length;
}

int main()
{
    char str1[] = "aaabbbccc";   // 期望的最大连续相同字符数 = 3 ('aaa')
    char str2[] = "abcccdddddd"; // 期望的最大连续相同字符数 = 6 ('ddddd')
    char str3[] = "abcd";        // 期望的最大连续相同字符数 = 1 (每个字符都是唯一的)

    // 打印结果
    printf("最大连续相同字符数 in \"%s\": %d\n", str1, max_same_char(str1));
    printf("最大连续相同字符数 in \"%s\": %d\n", str2, max_same_char(str2));
    printf("最大连续相同字符数 in \"%s\": %d\n", str3, max_same_char(str3));

    return 0;
}
