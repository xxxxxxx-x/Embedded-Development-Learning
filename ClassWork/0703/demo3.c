#include <stdio.h>

/****************************************************************************************************************************************************************
 * @name: GetPhaseNum
 * @brief: 找到满足一定条件的阶梯数。
 * @return {int} - 返回满足条件的阶梯数。
 * @date: 2024-07-09 13:15:05
 * @version: 1.0
 * @note: 此函数采用循环方式逐个尝试找到满足除以2余1，除以3余2，除以5余4，除以6余5，除以7余0的最小正整数。
*****************************************************************************************************************************************************************/
int GetPhaseNum()
{
    int StairsNum = 0;  // 初始化阶梯数

    // 循环查找符合条件的阶梯数
    while (1)
    {
        // 检查是否同时满足所有除法条件
        if (StairsNum % 2 == 1 && StairsNum % 3 == 2 && StairsNum % 5 == 4 && StairsNum % 6 == 5 && StairsNum % 7 == 0)
        {
            break;  // 找到符合条件的阶梯数，退出循环
        }

        StairsNum++;  // 继续尝试下一个整数
    }

    return StairsNum;  // 返回符合条件的阶梯数
}

int main()
{
    int result = GetPhaseNum();  // 调用函数获取符合条件的阶梯数
    printf("The phase number is: %d\n", result);  // 打印符合条件的阶梯数
    return 0;  // 返回成功
}
