#include <stdio.h>

/****************************************************************************************************************************************************************
 * @name: printTriangle
 * @brief: 根据输入的整数 n 打印倒立的三角形。
 * @param {int} n - 要打印的三角形的行数。
 * @return {*}
 * @date: 2024-07-09 13:14:01
 * @version: 1.0
 * @note: 如果 n 为 0 或负数，函数不执行任何操作。
*****************************************************************************************************************************************************************/
void printTriangle(int n) {
    int spaces = 0;  // 记录每行开始前打印空格的数量

    // 每次循环打印一行，n 表示当前行要打印的 '*' 数量
    while (n > 0) {
        // 打印行前的空格
        for (int i = 0; i < spaces; i++) {
            printf(" ");
        }

        // 打印当前行的 '*'
        for (int i = 0; i < n; i++) {
            printf("*");
        }

        // 减少下一行要打印的 '*' 数量（每行减少两个 '*'）
        n -= 2;

        // 打印换行符
        printf("\n");

        // 增加下一行开始前的空格数
        spaces++;
    }
}

int main() {
    int test_cases[] = {8, 7, 6, 5, 4, 3, 2, 1}; // 可以根据需要添加更多的测试用例

    // 遍历测试用例数组，打印每个测试用例对应的倒立三角形
    for (int i = 0; i < sizeof(test_cases) / sizeof(test_cases[0]); i++) {
        printf("Triangle for n = %d:\n", test_cases[i]);
        printTriangle(test_cases[i]);  // 调用打印倒立三角形的函数
        printf("\n");  // 打印空行，用于分隔不同测试用例的输出
    }

    return 0;
}
