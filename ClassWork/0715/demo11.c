#include <stdio.h>

// 递归函数，计算从1到n的整数和
int sumToN(int n);

int main(int argc, const char *argv[]) {
    int n;

    // 提示用户输入一个正整数
    printf("请输入一个正整数：");
    // 读取用户输入
    if (scanf("%d", &n) != 1 || n <= 0) {
        printf("输入无效，请输入一个正整数。\n");
        return 1; // 返回非零值表示程序异常终止
    }

    // 计算并输出1到n之间所有正整数的和
    printf("1到%d之间所有正整数的和是：%d\n", n, sumToN(n));
    return 0; // 返回0表示程序正常终止
}

// 递归函数定义
int sumToN(int n) {
    if (n == 1) { // 递归基例
        return 1;
    }
    return n + sumToN(n - 1); // 递归调用
}
