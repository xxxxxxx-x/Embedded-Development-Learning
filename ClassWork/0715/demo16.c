/**************************************************************************************
 * @file         : demo16.c
 * @brief        : 输入一个正整数，输出它的各位数字之和。
 *                 注意：此代码假设输入的是正整数，不处理负数和零的情况。
 *                 各位数字之和是指将输入的正整数的每一位数字相加得到的结果。
 *                 例如，输入123，则各位数字之和为1 + 2 + 3 = 6。
 *                 如果输入的是零，则各位数字之和也是零。
 *                 如果输入的是一个很大的正整数，确保代码能正确处理各位数字之和。
 *                 如果输入的是一个小数，此代码不处理小数部分。
 *                 此代码假设输入的数是有效的正整数。
 *                 各位数字之和的计算通过循环和数学运算实现。
 *                 输出结果包括原始输入的数和各位数字之和。
 * @author       : a_23456@foxmail.com
 * @date         : 2024-07-15 20:19:56
 * @version      : 1.0
 * @note         :
 *                 这段代码从用户获取一个正整数，并计算输出其各位数字之和。
 *                 通过循环和数学运算实现各位数字的提取和相加。
 *                 输出结果包括原始输入的数和各位数字之和。
 **************************************************************************************/

#include <stdio.h>

int main(int argc, const char *argv[]) {
    unsigned int number, originalNumber, sum = 0;

    printf("请输入一个正整数：");
    scanf("%u", &number);

    originalNumber = number; // 保存原始输入的数，用于最后输出

    // 计算各位数字之和
    while (number > 0) {
        sum += number % 10; // 取出最后一位数字并加到sum中
        number /= 10;       // 去掉已经处理过的最后一位数字
    }

    printf("%u的各位数字之和是：%u\n", originalNumber, sum);

    return 0;
}
