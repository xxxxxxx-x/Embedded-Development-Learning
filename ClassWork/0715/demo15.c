/**************************************************************************************
 * @file         : demo15.c
 * @brief        : 小练习：实现一个程序，逆序输出一个整数的各位数字。
 *                 注意：此代码假设输入的是正整数，不处理负数和零的情况。
 *                 逆序输出一个整数的各位数字是指，将输入的整数的每一位数字从个位到最高位逐个输出。
 *                 例如，输入123，则逆序输出为321。
 *                 如果输入的是零，则输出结果也是零。
 *                 如果输入的是一个很大的正整数，确保代码能正确处理各位数字的逆序输出。
 *                 如果输入的是一个小数，此代码不处理小数部分。
 *                 此代码假设输入的数是有效的正整数。
 *                 逆序输出各位数字通过循环和数学运算实现。
 *                 输出结果包括逆序输出的整数各位数字。
 * @date         : 2024-07-15 20:04:48
 * @version      : 1.0
 * @note         :
 *                 这段代码从用户获取一个正整数，并逆序输出其各位数字。
 *                 通过循环和数学运算实现逆序输出各位数字。
 **************************************************************************************/

#include <stdio.h>

int main(int argc, const char *argv[]) {
    unsigned int number;

    printf("请输入一个正整数：");
    scanf("%u", &number);

    // 逆序输出各位数字
    printf("逆序输出的结果是：");
    while (number > 0) {
        printf("%u", number % 10); // 输出当前数字的个位数
        number /= 10;              // 去掉已经处理过的最后一位数字
    }
    
    printf("\n"); // 输出换行符，使输出结果更清晰

    return 0;
}
