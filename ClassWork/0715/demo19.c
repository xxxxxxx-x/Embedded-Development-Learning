#include <stdio.h>

int main(int argc, const char *argv[]) {
    double number;

    // 提示用户输入一个数
    printf("请输入一个实数：");
    scanf("%lf", &number);

    // 计算并输出绝对值
    double absolute_value = (number < 0) ? -number : number;
    printf("输入的数的绝对值是：%.2f\n", absolute_value);

    return 0;
}
