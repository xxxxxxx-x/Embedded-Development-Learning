#include <stdio.h>

int main() {
    int number;

    // 提示用户输入一个整数
    printf("请输入一个整数：");
    scanf("%d", &number);

    // 打印从number到number + 10的所有整数，以空格分隔
    for (int i = 0; i <= 10; ++i) {
        printf("%d ", number + i);
    }
    printf("\n");

    return 0;
}
