#include <stdio.h>

// 函数声明：计算并打印浮点数的立方值
void cube(float number);

int main() {
  float num;

  // 提示用户输入一个小数
  printf("请输入一个小数：");
  scanf("%f", &num);

  // 调用函数计算并打印立方值
  cube(num);

  return 0;
}

// 函数定义：计算并打印浮点数的立方值
void cube(float number) {
  double result = number * number * number; // 计算立方值
  printf("这个立方值是：%lf\n", result);    // 打印立方值
}
